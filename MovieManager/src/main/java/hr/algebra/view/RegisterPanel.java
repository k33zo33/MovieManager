    private void init() {
        try {
            initValidation();
            hideErrors();
            initRepository();

        } catch (Exception ex) {
            Logger.getLogger(RegisterPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Unrecoverable error", "Cannot initiate the form");
            System.exit(1);
        }
    }

    public void addLoginInterface(LoginInterface service) {
        this.loginInterface = service;
    }

    private void initValidation() {
        validationFields = Arrays.asList(tfUsername, tfPassword, tfPasswordRepeat);
        errorLabels = Arrays.asList(lbUsernameError, lbPasswordError, lbPasswordRepeatError);
    }

    private void hideErrors() {
        errorLabels.forEach(e -> e.setVisible(false));
    }

    private void initRepository() {
        userRepostiory = new UserRepository();
    }

    private boolean formValid() {
        hideErrors();
        boolean ok = true;

        for (int i = 0; i < validationFields.size(); i++) {
            ok &= !validationFields.get(i).getText().trim().isEmpty();
            errorLabels.get(i).setVisible(validationFields.get(i).getText().trim().isEmpty());

        }
        // Provjera jesu li zadnje dvije lozinke iste
        if (validationFields.size() > 1) {
            int lastIndex = validationFields.size() - 1;
            int secondLastIndex = lastIndex - 1;

            if (!validationFields.get(lastIndex).getText().equals(validationFields.get(secondLastIndex).getText())) {
                ok = false;
                errorLabels.get(lastIndex).setVisible(true);
            }
        }

        return ok;
    }

    private void clearForm() {
        hideErrors();
        validationFields.forEach(e -> e.setText(""));
    }
