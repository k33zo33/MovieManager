    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if (!formValid()) {

            return;
        }
        try {
            user = new User(
                    tfUsername.getText().trim(),
                    tfPassword.getText().trim(),
                    2
            );
            int userId = userRepostiory.create(user);
            user.setId(userId);
            loginInterface.userLogIn(user);

            clearForm();

        } catch (Exception ex) {
            Logger.getLogger(RegisterPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to register account!");
        }
    }//GEN-LAST:event_btnRegisterActionPerformed


